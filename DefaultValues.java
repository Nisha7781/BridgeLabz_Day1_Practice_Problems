class DefaultValues 
{
    
    static boolean val1;
    static double val2;
    static float val3;
    static int val4;
    static long val5;
    static String val6;
    static short val7;
    static char val8;
    public static void main(String[] args) 
    {
        System.out.println("Default values of primitive datatypes..");
        System.out.println("boolean = " + val1);
        System.out.println("double = " + val2);
        System.out.println("float = " + val3);
        System.out.println("int = " + val4);
        System.out.println("long = " + val5);
        System.out.println("string = " + val6);
        System.out.println("short = " + val7);
        System.out.println("char = " + val8);
    }
}
